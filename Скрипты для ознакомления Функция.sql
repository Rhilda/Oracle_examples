CREATE OR REPLACE FUNCTION BOOL_TO_CHAR(INBL IN BOOLEAN) RETURN VARCHAR2
IS
OUT_ST VARCHAR2(5);
BEGIN
	IF (INBL) THEN
	OUT_ST := 'TRUE';
	ELSIF (NOT INBL) THEN
	OUT_ST := 'FALSE';
	ELSE 
	OUT_ST := 'NULL';
	END IF;

	RETURN(OUT_ST);
END BOOL_TO_CHAR;




CREATE OR REPLACE FUNCTION FACTORIAL(NUM IN NUMBER) RETURN NUMBER
IS
BEGIN
IF (NUM <=1) THEN
 RETURN (NUM);
ELSE
 RETURN (NUM * FACTORIAL(NUM-1));

END IF;
END FACTORIAL;

